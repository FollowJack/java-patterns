package infrastructure.iterator;

import java.util.Iterator;

/**
 * Created by Deniel on 16.10.2014.
 */
public class NullIterator implements Iterator {
    @Override
    public boolean hasNext() {
        return false;
    }

    @Override
    public Object next() {
        return null;
    }

    @Override
    public void remove() {
        throw new UnsupportedOperationException();
    }
}
